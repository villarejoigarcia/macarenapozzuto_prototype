export const POSTS_QUERY = `*[_type=="post" && defined(slug.current)] | order(order asc){
  _id,
  title,
  year,
  slug,
  description,
  credits,
  publishedAt,
  categories[]->{title},
  cover{
    _type,
    asset->{
      url
    }
  },
  images[]{
    _type,
    asset->{
      url
    }
  }
}`;